def check_dependencies_for_ending(key):
    """
    Checks dependencies and happiness to determine if an alternate ending should be triggered.

    Args:
        key (str): The ending key to check.

    Returns:
        str: The original or alternate ending key. Possible alternate endings:
            - "lonely_addiction" if dependencies are high for happy endings.
            - "rehab" if dependencies are moderate for hope ending.
            - "bittersweet" if happiness is low for hope ending.
            - Otherwise, returns the original key.
    """
    global dependencies, happiness
    if key in ["happy_family", "happy_alone"]:
        if dependencies["Cigarettes"] > 3 or dependencies["Alcohol"] > 3:
            slow_print("Your addictions sabotaged your relationships...")
            return "lonely_addiction"
    if key == "hope":
        if dependencies["Cigarettes"] > 2 or dependencies["Alcohol"] > 2:
            return "rehab"
        elif happiness <= 40:
            return "bittersweet"
    return key

